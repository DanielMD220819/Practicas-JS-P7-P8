function animal() {
    const pet = "dog";

    if (pet === "lizard") {
        document.write("I own a lizard");
    } else if (pet === "dog") {
        document.write("I own a dog");
    } else if (pet === "cat") {
        document.write("I own a cat");
    } else if (pet === "snake") {
        document.write("I own a snake");
    } else if (pet === "parrot") {
        document.write("I own a parrot");
    } else {
        document.write("I don't own a pet");
    }
}