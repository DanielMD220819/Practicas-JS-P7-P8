function MayorQue(numero1, numero2) {
    if (numero1 == numero2) {
        document.write("Los dos números son iguales")
    } else {
        if (numero1 > numero2) {
            document.write("El primer número es mayor que el segundo")
        } else {
            document.write("El primer número es menor que el segundo")
        }
    }
}